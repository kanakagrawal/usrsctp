#define SCTP_CRC32C(c,d) (c=(c>>8)^sctp_crc_c[(c^(d))&0xFF])

static uint32_t
singletable_crc32c(uint32_t crc32c,
                   const unsigned char *buffer,
                   unsigned int length)
{
	unsigned int i;

	for (i = 0; i < length; i++) {
		SCTP_CRC32C(crc32c, buffer[i]);
	}
	return (crc32c);
}

